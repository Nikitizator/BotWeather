def advice_temp(temp):
    if temp < -30:
        return "Экстремальный мороз! 🧊 Опасность обморожения за 10 минут.\n Выходите только при крайней необходимости."
    elif temp < -20:
        return "Сильный холод. Термобелье, шерстяные носки и плотная шапка обязательны. 🧣"
    elif temp < -10:
        return "Морозно. Куртка на меху или пуховик — лучший выбор. Не забудьте варежки! 🧤"
    elif temp < 0:
        return "Легкий минус. Подойдет зимняя куртка или плотное пальто. Под низ - теплый свитер."
    elif temp < 10:
        return "Прохладно. Переходим на демисезон: куртка с утеплителем, легкий шарф. 🧥"
    elif temp < 18:
        return "Свежо. Ветровка или плотное худи будут в самый раз. Идеально для прогулок! 👟"
    elif temp < 25:
        return "Тепло и комфортно. Можно надеть футболку и легкие брюки. Наслаждайтесь! ✨"
    elif temp < 30:
        return "Жарко. Выбирайте одежду из натуральных тканей (лен, хлопок). Пейте больше воды! 🥤"
    else:
        return "Пекло! 🔥 Старайтесь быть в тени. Головной убор — ваш главный союзник."

def advice_wind(wind_kmph):
    wind = int(wind_kmph)
    if wind < 5:
        return "Полный штиль. Воздух замер, дышится легко. ✨"
    elif wind < 15:
        return "Легкий ветерок. Приятно освежает, но капюшон не помешает."
    elif wind < 30:
        return "Ощутимый ветер. 🌬️ Зонты может выворачивать, а уши — надуть."
    elif wind < 55:
        return "Сильный ветер! Возможны падения веток. Держитесь подальше от рекламных щитов. 💨"
    else:
        return "Штормовое предупреждение! ⚠️ Порывы могут сбивать с ног. Лучше переждать дома."

def advice_visibility(vis_km):
    vis = int(vis_km)
    if vis < 0.5:
        return "Экстремальный туман! "
    elif vis < 2:
        return "Густая дымка. Ориентиры теряются, будьте предельно осторожны. 😶‍🌫️"
    elif vis < 10:
        return "Видимость средняя. Горизонт немного размыт."
    else:
        return "Кристально чистое небо! Видно всё до горизонта. 🔭"

def advice_status(status_text):
    status = status_text.lower()
    if "rain" in status or "дождь" in status:
        return "Возьми зонт или дождевик, обещают дождь! ☔"
    elif "snow" in status or "снег" in status:
        return "На улице снежок. Самое время для перчаток! 🧤"
    elif "clear" in status or "ясно" in status or "sunny" in status:
        return "На небе солнышко, не забудь темные очки. 😎"
    elif "cloudy" in status or "облачно" in status:
        return "Небо затянуто облаками, солнца не видно. ☁️"
    else:
        return "Погода переменчива, будь готов ко всему."


def advice_range(min_t, max_t):
    diff = int(max_t) - int(min_t)
    if diff > 10:
        return f"Большой перепад температур (от {min_t}°C до {max_t}°C).\nОдевайся с умом, чтобы можно было снять лишнее! 👕🧥"
    else:
        return "Температура в течение дня будет стабильной."

def advice_pressure_mm(mm):
    if mm < 740:
        return ("⚠️ Низкое давление: возможна слабость.")
    elif mm > 760:
        return ("⚠️ Высокое давление: берегите голову.")
    else:
        return ("✅ Давление в норме.")

def advice_humidity(hum_percent):
    h = int(hum_percent)
    if h < 30:
        return "Воздух очень сухой. Используйте увлажняющий крем для лица и пейте воду. 🧴"
    elif h > 70:
        return "Высокая влажность. Жару будет переносить сложнее,\n а холод покажется более 'колючим'. 💦"
    else:
        return "Влажность в комфортных пределах (40-60%)."

def advice_uv(uv):
    uv = float(uv)
    if uv < 3:
        return "Низкий уровень УФ. Защита не требуется. ✅"
    elif uv < 6:
        return "Средний уровень УФ. Используйте солнцезащитный крем и наденьте кепку. 🧢"
    elif uv < 8:
        return "⚠️ Высокий уровень! Обязательно наденьте очки и не находитесь на солнце долго. 🕶️"
    else:
        return "☢️ Экстремальный УФ! Оставайтесь в тени, кожа может сгореть за считанные минуты!"

def advice_daylight(sunrise, sunset):

    import datetime
    try:
        start = datetime.datetime.strptime(sunrise, "%H:%M")
        end = datetime.datetime.strptime(sunset, "%H:%M")
        duration = (end - start).seconds // 3600
        if duration < 8:
            return "Световой день очень короткий. Планируйте дела на утро! ☀️"
        else:
            return "Длинный день — отличная возможность для вечерней прогулки. 🚶‍♂️"
    except:
        return "Планируйте свой день с учетом заката."
